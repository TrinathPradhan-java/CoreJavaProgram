package com.nit.lab.assignment4;

public class AreaofCylinder {

	public static void main(String[] args) {
	double pi = 3.14;
	double height = 25;
	double r = 15;
	double areaofCylinder;
	areaofCylinder = pi*(r*r) + (pi *r * height);
	System.out.println("Area of Cylinder is :"+areaofCylinder);

	}

}
